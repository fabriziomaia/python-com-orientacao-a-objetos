# 1. Inteiro (int)
idade = 20
ano = 2010

print(type(idade))
print(type(ano))

# 2. Decimal (float)
altura = 1.73
peso = 78.500

print(type(peso))
print(type(altura))

# 3. Complexo (complex)
a = 10+16j
b = 3+80j

print(type(a))
print(type(b))

# 4. String (str)
nome = "Guilherme"
profissao = "Engenheiro de Software"

print(type(profissao ))
print(type(nome))

# 5. Boolean (bool)
sexta_feira = True
feriado = False

print(type(sexta_feira))
print(type(feriado))

# 6. Listas (list)
alunos = ["Mônica"", ""Ana"", ""Bruno", "Alice"]
notas = [10, 8.5, 7.8, 8.0]

print(type(alunos))
print(type(notas))

# 7. Tuplas (tuple)
valores = (80, 29, 45, 91, 23)
pontos = (10, 29.05, 66.8, 72)

print(type(valores))
print(type(pontos))

# 8. Dicionários (dict)
altura={"Sandra":1.65, "Elizabeth": 1.60, "Roberto": 1.70}
idade = {"Sandra":35, "Elizabeth":58, "Roberto":68}

print(type(altura))
print(type(idade))
